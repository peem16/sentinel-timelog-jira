const GITHUB_SCOPES: &str = "repo read:org read:user";
const GITHUB_TOKEN_URL: &str = "https://github.com/login/oauth/access_token";

/// GitHub OAuth tokens are non-expiring by default (no `expires_in`/refresh
/// token) unless the OAuth app opts into token expiration — handle both.
fn parse_github_tokens(v: &Value, prev_refresh: Option<&str>) -> Result<Tokens, String> {
    if let Some(err) = v["error"].as_str() {
        let desc = v["error_description"].as_str().unwrap_or("");
        return Err(format!("GitHub token error: {err} {desc}"));
    }
    let access = v["access_token"]
        .as_str()
        .ok_or_else(|| format!("แลก token ไม่สำเร็จ: {}", short(v)))?;
    let refresh = v["refresh_token"]
        .as_str()
        .map(|s| s.to_string())
        .or_else(|| prev_refresh.map(|s| s.to_string()))
        .unwrap_or_default();
    let expires_at = match v["expires_in"].as_i64() {
        Some(secs) => Utc::now().timestamp() + secs - 60,
        // no expiry advertised — treat as effectively permanent (~10 years)
        None => Utc::now().timestamp() + 10 * 365 * 24 * 3600,
    };
    Ok(Tokens {
        access_token: access.to_string(),
        refresh_token: refresh,
        expires_at,
    })
}

pub async fn connect_github(app: &AppHandle) -> Result<GitHubConn, String> {
    let state = app.state::<AppState>();
    let s = state.settings.read().await.clone();
    let client_id = github_client_id(&s);
    let client_secret = github_client_secret(&s);
    if client_id.is_empty() || client_secret.is_empty() {
        return Err(
            "ยังไม่มี GitHub OAuth credentials — build โดยตั้ง TIMELOG_GITHUB_CLIENT_ID/SECRET หรือกรอกในหัวข้อ 'OAuth App'".into(),
        );
    }
    let state_param = rand_string(32);
    let url = format!(
        "https://github.com/login/oauth/authorize?client_id={}&redirect_uri={}&scope={}&state={}",
        urlencoding::encode(&client_id),
        urlencoding::encode(REDIRECT_URI),
        urlencoding::encode(GITHUB_SCOPES),
        state_param
    );
    open::that(&url).map_err(|e| format!("เปิดเบราว์เซอร์ไม่ได้: {e}"))?;
    let code = listen_for_code(&state_param).await?;

    let http = reqwest::Client::new();
    let res: Value = http
        .post(GITHUB_TOKEN_URL)
        .header("Accept", "application/json")
        .form(&[
            ("client_id", client_id.as_str()),
            ("client_secret", client_secret.as_str()),
            ("code", code.as_str()),
            ("redirect_uri", REDIRECT_URI),
        ])
        .send()
        .await
        .map_err(|e| e.to_string())?
        .json()
        .await
        .map_err(|e| e.to_string())?;
    let tokens = parse_github_tokens(&res, None)?;

    // discover the logged-in user's login handle (needed for search qualifiers)
    let user: Value = http
        .get("https://api.github.com/user")
        .bearer_auth(&tokens.access_token)
        .header("User-Agent", "timelog")
        .header("Accept", "application/vnd.github+json")
        .send()
        .await
        .map_err(|e| e.to_string())?
        .json()
        .await
        .unwrap_or(Value::Null);
    let login = user["login"].as_str().unwrap_or_default().to_string();
    if login.is_empty() {
        return Err("อ่าน GitHub login ไม่ได้จาก token".into());
    }

    let conn = GitHubConn { tokens, login };
    state.connections.write().await.github = Some(conn.clone());
    persist(app).await?;
    Ok(conn)
}

/// Return a GitHub connection with a fresh access token, refreshing if needed.
pub async fn fresh_github(app: &AppHandle) -> Result<GitHubConn, String> {
    let state = app.state::<AppState>();
    let conn = state
        .connections
        .read()
        .await
        .github
        .clone()
        .ok_or("ยังไม่ได้เชื่อมต่อ GitHub")?;
    if conn.tokens.expires_at > Utc::now().timestamp() {
        return Ok(conn);
    }
    // expired — only OAuth apps with token expiration give us a refresh token.
    // Without one there's nothing to refresh; return as-is and let the API 401.
    if conn.tokens.refresh_token.is_empty() {
        return Ok(conn);
    }
    let s = state.settings.read().await.clone();
    let http = reqwest::Client::new();
    let res: Value = http
        .post(GITHUB_TOKEN_URL)
        .header("Accept", "application/json")
        .form(&[
            ("grant_type", "refresh_token"),
            ("client_id", github_client_id(&s).as_str()),
            ("client_secret", github_client_secret(&s).as_str()),
            ("refresh_token", conn.tokens.refresh_token.as_str()),
        ])
        .send()
        .await
        .map_err(|e| e.to_string())?
        .json()
        .await
        .map_err(|e| e.to_string())?;
    let tokens = parse_github_tokens(&res, Some(&conn.tokens.refresh_token))
        .map_err(|e| format!("ต่ออายุ GitHub token ไม่ได้ ลอง Login ใหม่: {e}"))?;
    let new_conn = GitHubConn { tokens, ..conn };
    state.connections.write().await.github = Some(new_conn.clone());
    persist(app).await?;
    Ok(new_conn)
}

