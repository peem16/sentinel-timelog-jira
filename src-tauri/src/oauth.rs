const GOOGLE_SCOPES: &str = "openid email https://www.googleapis.com/auth/calendar.readonly";

pub async fn connect_google(app: &AppHandle) -> Result<GoogleConn, String> {
    let state = app.state::<AppState>();
    let s = state.settings.read().await.clone();
    let client_id = google_client_id(&s);
    let client_secret = google_client_secret(&s);
    if client_id.is_empty() || client_secret.is_empty() {
        return Err(
            "ยังไม่มี Google OAuth credentials — build โดยตั้ง TIMELOG_GOOGLE_CLIENT_ID/SECRET หรือกรอกในหัวข้อ 'OAuth App'".into(),
        );
    }
    let state_param = rand_string(32);
    let verifier = rand_string(64);
    let challenge = base64::engine::general_purpose::URL_SAFE_NO_PAD
        .encode(Sha256::digest(verifier.as_bytes()));
    let url = format!(
        "https://accounts.google.com/o/oauth2/v2/auth?client_id={}&redirect_uri={}&response_type=code&scope={}&access_type=offline&prompt=consent&code_challenge={}&code_challenge_method=S256&state={}",
        urlencoding::encode(&client_id),
        urlencoding::encode(REDIRECT_URI),
        urlencoding::encode(GOOGLE_SCOPES),
        challenge,
        state_param
    );
    open::that(&url).map_err(|e| format!("เปิดเบราว์เซอร์ไม่ได้: {e}"))?;
    let code = listen_for_code(&state_param).await?;

    let http = reqwest::Client::new();
    let res: Value = http
        .post("https://oauth2.googleapis.com/token")
        .form(&[
            ("grant_type", "authorization_code"),
            ("client_id", client_id.as_str()),
            ("client_secret", client_secret.as_str()),
            ("code", code.as_str()),
            ("redirect_uri", REDIRECT_URI),
            ("code_verifier", verifier.as_str()),
        ])
        .send()
        .await
        .map_err(|e| e.to_string())?
        .json()
        .await
        .map_err(|e| e.to_string())?;
    let tokens = parse_tokens(&res, None)?;

    let userinfo: Value = http
        .get("https://openidconnect.googleapis.com/v1/userinfo")
        .bearer_auth(&tokens.access_token)
        .send()
        .await
        .map_err(|e| e.to_string())?
        .json()
        .await
        .unwrap_or(Value::Null);
    let email = userinfo["email"].as_str().unwrap_or_default().to_string();

    let conn = GoogleConn { tokens, email };
    state.connections.write().await.google = Some(conn.clone());
    persist(app).await?;
    Ok(conn)
}

/// Return a Google connection with a fresh access token, refreshing if needed.
pub async fn fresh_google(app: &AppHandle) -> Result<GoogleConn, String> {
    let state = app.state::<AppState>();
    let conn = state
        .connections
        .read()
        .await
        .google
        .clone()
        .ok_or("ยังไม่ได้เชื่อมต่อ Google Calendar")?;
    if conn.tokens.expires_at > Utc::now().timestamp() {
        return Ok(conn);
    }
    let s = state.settings.read().await.clone();
    let cid = google_client_id(&s);
    let csecret = google_client_secret(&s);
    let http = reqwest::Client::new();
    let res: Value = http
        .post("https://oauth2.googleapis.com/token")
        .form(&[
            ("grant_type", "refresh_token"),
            ("client_id", cid.as_str()),
            ("client_secret", csecret.as_str()),
            ("refresh_token", conn.tokens.refresh_token.as_str()),
        ])
        .send()
        .await
        .map_err(|e| e.to_string())?
        .json()
        .await
        .map_err(|e| e.to_string())?;
    // Google keeps the same refresh token (not returned on refresh)
    let tokens = parse_tokens(&res, Some(&conn.tokens.refresh_token))
        .map_err(|e| format!("ต่ออายุ Google token ไม่ได้ ลอง Login ใหม่: {e}"))?;
    let new_conn = GoogleConn { tokens, ..conn };
    state.connections.write().await.google = Some(new_conn.clone());
    persist(app).await?;
    Ok(new_conn)
}

