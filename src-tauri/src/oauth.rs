/// Build a JiraClient — prefers the OAuth connection, falls back to
/// manual basic-auth settings if OAuth was never connected.
pub async fn jira_client(app: &AppHandle) -> Result<JiraClient, String> {
    let state = app.state::<AppState>();
    let has_oauth = state.connections.read().await.atlassian.is_some();
    if has_oauth {
        let conn = fresh_atlassian(app).await?;
        return JiraClient::bearer(
            format!("https://api.atlassian.com/ex/jira/{}", conn.cloud_id),
            conn.tokens.access_token,
        );
    }
    let s = state.settings.read().await.clone();
    JiraClient::from_settings(&s).map_err(|_| {
        "ยังไม่ได้เชื่อมต่อ Jira — กด 'Login ด้วย Atlassian' ในแท็บตั้งค่า".to_string()
    })
}
