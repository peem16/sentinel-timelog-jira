/* ================= app credentials =================
 * Prefer credentials baked in at build time (from .env / env vars, never
 * committed) so end users only click "Login". Fall back to values typed in the
 * settings UI. Register the OAuth apps once; build with the .env filled in.
 *
 * EMBED_* constants are generated by build.rs into OUT_DIR/credentials.rs. */

include!(concat!(env!("OUT_DIR"), "/credentials.rs"));

fn embedded_or(embedded: Option<&str>, fallback: &str) -> String {
    match embedded {
        Some(v) if !v.trim().is_empty() => v.trim().to_string(),
        _ => fallback.trim().to_string(),
    }
}

pub fn atlassian_client_id(s: &Settings) -> String {
    embedded_or(EMBED_ATLASSIAN_CLIENT_ID, &s.atlassian_client_id)
}
pub fn atlassian_client_secret(s: &Settings) -> String {
    embedded_or(EMBED_ATLASSIAN_CLIENT_SECRET, &s.atlassian_client_secret)
}
pub fn google_client_id(s: &Settings) -> String {
    embedded_or(EMBED_GOOGLE_CLIENT_ID, &s.google_client_id)
}
pub fn google_client_secret(s: &Settings) -> String {
    embedded_or(EMBED_GOOGLE_CLIENT_SECRET, &s.google_client_secret)
}
pub fn github_client_id(s: &Settings) -> String {
    embedded_or(EMBED_GITHUB_CLIENT_ID, &s.github_client_id)
}
pub fn github_client_secret(s: &Settings) -> String {
    embedded_or(EMBED_GITHUB_CLIENT_SECRET, &s.github_client_secret)
}

/// True when this build has the provider's id+secret compiled in — the UI
/// uses this to hide the manual Client ID/Secret fields entirely.
pub fn atlassian_embedded() -> bool {
    !embedded_or(EMBED_ATLASSIAN_CLIENT_ID, "").is_empty()
        && !embedded_or(EMBED_ATLASSIAN_CLIENT_SECRET, "").is_empty()
}
pub fn google_embedded() -> bool {
    !embedded_or(EMBED_GOOGLE_CLIENT_ID, "").is_empty()
        && !embedded_or(EMBED_GOOGLE_CLIENT_SECRET, "").is_empty()
}
pub fn github_embedded() -> bool {
    !embedded_or(EMBED_GITHUB_CLIENT_ID, "").is_empty()
        && !embedded_or(EMBED_GITHUB_CLIENT_SECRET, "").is_empty()
}
